//for 반복문
for (let i = 0; i < 10; i++) {
    console.log('지금 현재 i는', i, '입니다');
}

// while 반복문 (조건이 참인 동안 반복)
let j = 0;
while (j < 10) {
    console.log('지금 현재 j는', j, '입니다');
    j++;
}

// do-while 반복문 (최소 한번은 실행됨)
let k = 0;
do {
    console.log('지금 현재 k는', k, '입니다');
    k++;
} while (k < 10);